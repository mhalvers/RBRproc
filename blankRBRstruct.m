function ctd = blankRBRstruct(n);

% Creates empty structure with correct fields for use with RBRproc
% functions.
%
%  usage:  
% 
%      ctd = blankRBRstruct(n);
%
%   where n specifies the number of profiles.
%
%  Mark Halverson, June 28, 2016

if nargin==0,
    n = 1;
end


ctd = struct('fileName',[],...
             'units',[],...
             'serialID',[],...
             'model',[],...
             'samplingPeriod',[],...
             'tzone',[],...
             'mtime',[],...
             'Latitude',[],...
             'Longitude',[],...
             'Station',[],...
             'profiles',[],...
             'Conductivity',[],...
             'Temperature',[],...
             'Pressure',[],...
             'PAR',[],...
             'Turbidity',[],...
             'Chlorophyll',[],...
             'DissolvedO2',[],...
             'Salinity',[],...
             'processingLog',[]);


ctd = repmat(ctd,1,n);
